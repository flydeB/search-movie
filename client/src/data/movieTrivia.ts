/**
 * 根据日期获取今日推荐电影（每天自动切换）
 */
export function getTodayMovie(): DailyMovie {
  const dayOfYear = getDayOfYear()
  return DAILY_MOVIES[dayOfYear % DAILY_MOVIES.length]
}

/**
 * 根据日期获取今日冷知识（每天自动切换）
 */
export function getTodayFact(): MovieFact {
  const dayOfYear = getDayOfYear()
  return MOVIE_FACTS[dayOfYear % MOVIE_FACTS.length]
}

/**
 * 获取一年中的第几天
 */
function getDayOfYear(): number {
  const now = new Date()
  const start = new Date(now.getFullYear(), 0, 0)
  const diff = now.getTime() - start.getTime()
  return Math.floor(diff / (1000 * 60 * 60 * 24))
}
